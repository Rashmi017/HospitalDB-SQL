CREATE DATABASE HospitalDB;
USE HospitalDB;

CREATE TABLE Diagnoses (
    DiagnosisID   INT PRIMARY KEY,
    DiagnosisName VARCHAR(255) NOT NULL
);

CREATE TABLE Outcomes (
    OutcomeID          INT PRIMARY KEY,
    OutcomeDescription VARCHAR(255) NOT NULL
);

CREATE TABLE Patients (
    PatientID     INT PRIMARY KEY,
    Name          VARCHAR(255),
    Age           INT,
    Gender        CHAR(1),
    DiagnosisID   INT,
    AdmissionDate DATE,
    DischargeDate DATE,
    OutcomeID     INT,
    TreatmentCost DECIMAL(10, 2),
    FOREIGN KEY (DiagnosisID) REFERENCES Diagnoses(DiagnosisID),
    FOREIGN KEY (OutcomeID)   REFERENCES Outcomes(OutcomeID)
);

CREATE TABLE Labs (
    LabID       INT PRIMARY KEY,
    PatientID   INT,
    TestName    VARCHAR(255),
    Result      DECIMAL(10, 2),
    NormalRange VARCHAR(50),
    FOREIGN KEY (PatientID) REFERENCES Patients(PatientID)
);


