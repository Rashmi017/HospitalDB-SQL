-- Query 1: View All Records
SELECT * FROM Patients;
SELECT * FROM Labs;
SELECT * FROM Diagnoses;
SELECT * FROM Outcomes;

-- Query 2: Retrieve Detailed Patient Lab History
SELECT p.PatientID, p.Name, d.DiagnosisName,
       o.OutcomeDescription, l.TestName, l.Result, l.NormalRange
FROM Patients p
JOIN Diagnoses d ON p.DiagnosisID = d.DiagnosisID
JOIN Outcomes  o ON p.OutcomeID   = o.OutcomeID
JOIN Labs      l ON p.PatientID   = l.PatientID
ORDER BY p.PatientID, l.TestName;

-- Query 3: Average Lab Results by Diagnosis
SELECT d.DiagnosisName, l.TestName, AVG(l.Result) AS AvgResult
FROM Patients  p
JOIN Diagnoses d ON p.DiagnosisID = d.DiagnosisID
JOIN Labs      l ON l.PatientID   = p.PatientID
GROUP BY d.DiagnosisName, l.TestName;

-- Query 4: Count of Abnormal Lab Records per Patient
SELECT p.PatientID, p.Name, COUNT(*) AS AbnormalCount
FROM Patients p
JOIN Labs l ON p.PatientID = l.PatientID
WHERE (l.TestName = 'Blood Pressure' AND (l.Result > 200  OR l.Result < 150))
   OR (l.TestName = 'Cholesterol'    AND (l.Result > 17   OR l.Result < 13))
   OR (l.TestName = 'Hemoglobin'     AND  l.Result > 1.2)
GROUP BY p.PatientID, p.Name
ORDER BY AbnormalCount DESC;

-- Query 5: Diagnosis with Highest Treatment Cost
SELECT d.DiagnosisName, SUM(p.TreatmentCost) AS TotalCost
FROM Patients  p
JOIN Diagnoses d ON p.DiagnosisID = d.DiagnosisID
GROUP BY d.DiagnosisName
ORDER BY TotalCost DESC;

-- Query 6: Patients at Risk by Age & Outcome
SELECT p.PatientID, p.Name, d.DiagnosisName, o.OutcomeDescription
FROM Patients  p
JOIN Diagnoses d ON p.DiagnosisID = d.DiagnosisID
JOIN Outcomes  o ON p.OutcomeID   = o.OutcomeID
WHERE p.Age > 30 AND o.OutcomeDescription != 'Recovered';

-- Query 7: Lab Trend Over Time for Specific Patients
SELECT l.TestName, l.Result, p.AdmissionDate
FROM Patients p
JOIN Labs l ON p.PatientID = l.PatientID
WHERE p.PatientID IN (2, 4, 6, 8, 12, 15)
ORDER BY p.AdmissionDate;

-- Query 8: Distribution of Outcomes by Diagnosis
SELECT d.DiagnosisName, o.OutcomeDescription, COUNT(*) AS OutcomeCount
FROM Patients  p
JOIN Diagnoses d ON p.DiagnosisID = d.DiagnosisID
JOIN Outcomes  o ON p.OutcomeID   = o.OutcomeID
GROUP BY d.DiagnosisName, o.OutcomeDescription
ORDER BY d.DiagnosisName, o.OutcomeDescription DESC;


